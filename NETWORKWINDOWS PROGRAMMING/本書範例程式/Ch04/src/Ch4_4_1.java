/* 程式範例: Ch4_4_1.java */
public class Ch4_4_1 {
    // 主程式
    public static void main(String[] args) {
        // 算術運算子
        System.out.println("負號運算: -6    = " + (-6) );
        System.out.println("正號運算: +6    = " + (+6) );
        System.out.println("乘法運算: 3 * 4 = " + (3 * 4));
        System.out.println("除法運算: 7.0 / 2.0 = " + (7.0 / 2.0));
        System.out.println("整數除法: 7 / 2 = " + (7 / 2));
        System.out.println("餘數運算: 7 % 2 = " + (7 % 2));
        System.out.println("加法運算: 5 + 3 = " + (5 + 3));
        System.out.println("減法運算: 5 - 3 = " + (5 - 3));
    }
}
