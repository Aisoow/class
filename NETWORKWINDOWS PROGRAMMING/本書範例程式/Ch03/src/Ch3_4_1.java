/* 程式範例: Ch3_4_1.java */
public class Ch3_4_1 {
    // 主程式
    public static void main(String[] args) {
        int i = 123;    // 宣告變數
        int j = -234;
        int k = 0256;
        int l = 0xccf;
        long m = 350000L;
        // 顯示變數值
        System.out.println("int i= " + i);
        System.out.println("int j= " + j);
        System.out.println("int k= " + k);
        System.out.println("int l= " + l);
        System.out.println("long m= " + m);
    }
}
