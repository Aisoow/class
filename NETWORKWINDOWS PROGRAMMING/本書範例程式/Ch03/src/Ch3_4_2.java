/* 程式範例: Ch3_4_2.java */
public class Ch3_4_2 {
    // 主程式
    public static void main(String[] args) {
        double i = 123.23;  // 宣告變數
        double j = 0.0007;
        double k = 5e4;
        double l = 4.34e-3;
        float m = 123.23F;
        // 顯示變數值/
        System.out.println("double i= " + i);
        System.out.println("double j= " + j);
        System.out.println("double k= " + k);
        System.out.println("double l= " + l);
        System.out.println("float m= " + m);
    }
}
