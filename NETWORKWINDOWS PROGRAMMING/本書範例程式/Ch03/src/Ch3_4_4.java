/* 程式範例: Ch3_4_4.java */
public class Ch3_4_4 {
    // 主程式
    public static void main(String[] args) {
        char a = 'A';  /// 宣告變數
        char b = 65;
        char c = '\u0020';
        char d = '\u0066';
        // 顯示變數值
        System.out.println("a= " + a);
        System.out.println("b= " + b);
        System.out.println("c= " + c);
        System.out.println("d= " + d);
        System.out.print("換行字元\n");
        System.out.print("\"Escape\"\t逸出字元\n");
    }
}
